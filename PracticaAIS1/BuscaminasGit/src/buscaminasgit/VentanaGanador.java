    public boolean hacerBackUpMejoresTiempos() {
        try{
             if ((this.n == 10) && (this.m == 10)){
                nombreFichero = this.directorioGlobal+this.separadorGlobal+"mejoresTiemposPrincipiante";
            } else if((this.n == 16) && (this.m == 16)){
                nombreFichero = this.directorioGlobal+this.separadorGlobal+"mejoresTiemposIntermedio";
            } else if((this.n == 32) && (this.m == 16)){
               nombreFichero = this.directorioGlobal+this.separadorGlobal+"mejoresTiemposExperto";
            }
        FileOutputStream fos = new FileOutputStream(nombreFichero);
        BufferedOutputStream bos = new BufferedOutputStream(fos);
        ObjectOutputStream oos = new ObjectOutputStream(bos);
        for(int i = 0; i < partidas.size(); i++){
            oos.writeObject(partidas.get(i));
        }
            oos.flush();
            oos.close();
            return true;
        } catch (IOException ex) {
            System.err.println("ERROR: No ha sido posible hacer el backup del fichero  '" + nombreFichero + "'");
        }
        return false;
    }
    
    public boolean restaurarBackUpMejoresTiempos() {
        try {
            // Apertura
            
            //String rutaGlobal = System.getProperty("file.separator");
            
             if ((this.n == 10) && (this.m == 10)){
                nombreFichero = this.directorioGlobal+this.separadorGlobal+"mejoresTiemposPrincipiante";
            } else if((this.n == 16) && (this.m == 16)){
                nombreFichero = this.directorioGlobal+this.separadorGlobal+"mejoresTiemposIntermedio";
            } else if((this.n == 32) && (this.m == 16)){
               nombreFichero = this.directorioGlobal+this.separadorGlobal+"mejoresTiemposExperto";
            }
            
            FileInputStream fis = new FileInputStream(nombreFichero);
            BufferedInputStream bis = new BufferedInputStream(fis);
            ObjectInputStream ois = new ObjectInputStream(bis);
            
            // Lectura
            
            Usuario u = (Usuario) ois.readObject();
            if(u != null){
                while(u != null){
                    partidas.add(u);
                    u = (Usuario) ois.readObject();
                } 
            }
            
            //this.setVisible(false);
            // Cierre
            
            ois.close();
            return true;
        } catch (EOFException ex) {
            //System.err.println("ERROR: No ha sido posible recuperar el backup del fichero '" + nombreFichero + "'. Fin de fichero inesperado");
            
        } catch (ClassNotFoundException | IOException ex) {
            System.err.println("ERROR: No ha sido posible recuperar el backup del fichero '" + nombreFichero + "' " + ex.getMessage());
        }
        return false;
    }
